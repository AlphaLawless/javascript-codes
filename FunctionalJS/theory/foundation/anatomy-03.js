// Anatomy Function Expression

// Arrow Function

// Function Expression
const increment = function (n) {
  return n + 1;
};

// Arrow function is always anonymous

// Arrow Function Option #1

const incre1 = (n) => {
  return n + 1;
};

// Arrow Function Option #2

const incre2 = (n) => {
  return n + 2;
};

// Arrow Function Option #3

const incre3 = (n) => n + 3;

// - - - -  -  - -  - -  - - - -

console.log(increment(1));
console.log(incre1(4));
console.log(incre2(5));
console.log(incre3(6));
